/* Functions in SQL */

-- Functions are the Database Objects that contains a set of SQL statements to perform a specific task
-- A function accept parameters, perform actions and then returns the result

-- Types of Function
-- 1. System Defined Function - These are built-in functions
-- -  Example - rand(), round(), upper(), lower(), count(), sum(), max(), etc.

-- 2. User Defined Function - Once you define a function you can call it the same way as the built-in functions

-- String Functions in SQL

-- String Functions are used to perform operation on input string and return output string

-- Commonly Used String Functions:

-- UPPER() - converts the value of field to upppercase
-- LOWER() - converts the value of field to lowercase
-- LENGTH() - returns the length of a value in text field
-- SUBSTRING() - extracts a substring from a string (str, start, end) - Include start and end both (1 based indexing in SQL)
-- NOW() - returns the current system date and time
-- FORMAT() - used to set a format of a field
-- CONCAT() - adds two or more fields together
-- REPLACE() - replaces all occurrences of a substring within a string, with a new substring
-- TRIM() - removes leading and trailing spaces (or other specified characters) from a string
